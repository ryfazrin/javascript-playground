/**
 * menggunakan tanda titik tiga kali (...) 
 * kemudian diikuti dengan nama variabel.
 */

var nilai1 = ["a", "b", "c", "d"];
console.log(nilai1); // Array [ "a", "b", "c", "d" ]

var nilai2 = [1, 2, 3, 4];
console.log(nilai2); // Array [ 1, 2, 3, 4 ]

var nilai3 = [...nilai1, "e", "f"];
console.log(nilai3);
